void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin){
  if(GPIO_Pin == UP_Pin){
    trackball.UP++;
  }
  else if(GPIO_Pin == DOWN_Pin){
    trackball.DOWN++;
  }
  else if(GPIO_Pin == LEFT_Pin){
    trackball.LEFT++;
  }
  else if(GPIO_Pin == RIGHT_Pin){
    trackball.RIGHT++;
  }
  else{
    __NOP();
  }
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim1){
  mouseHID mouse_data = {0,0,0,0};
  int16_t x = 0;
  int16_t y = 0;

  x = trackball.RIGHT - trackball.LEFT;
  y = trackball.UP - trackball.DOWN;

  mouse_data.mouse_x = filter_update(&filterx, 20 * x);
  mouse_data.mouse_y = filter_update(&filtery, 20 * y);

  USBD_HID_SendReport(&hUsbDeviceFS, (uint8_t *)&mouse_data, sizeof(mouse_data));
  ball_reset(&trackball);
}

